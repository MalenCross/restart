import { BracketTax,W2Tax } from "../TaxInfo2";



test(' get taxes owed, marginal tax rate, efective tax rate', () => {
    expect(W2Tax(9345)).toEqual([0, .10, 0]);
    expect(W2Tax(30000)).toEqual([1840.38, .12, .06]);
    expect(W2Tax(75000)).toEqual([9267.66, .22, .12]);
    expect(W2Tax(150000)).toEqual([26726.92, .24, .18]);
    expect(W2Tax(200000)).toEqual([40086.60, .32, .20]);
    expect(W2Tax(500000)).toEqual([144219.25, .35, .29]);
    expect(W2Tax(750000)).toEqual([235661.88, .37, .31]);
    expect(W2Tax(1000000)).toEqual([328161.88, .37, .33]);
    expect(W2Tax(1500000)).toEqual([513161.88, .37, .34]);
    expect(W2Tax(10000000)).toEqual([3658161.88, .37, .37]);
});

